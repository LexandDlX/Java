package com.Krakedev;

public class Calculadora {
	
	public int sumar (int valor1, int valor2) { //función
		
		int resultado; //declaro la variable
		
		resultado = valor1 + valor2;
		
		return resultado;
	}
	
	public double restar (int valor1, int valor2) { //función
		
		double resultado; //declaro la variable
		
		resultado = valor1 - valor2;
		
		return resultado;
	}
	
	public double multiplicar (double valor1, double valor2) { //función
		
		double resultado; //declaro la variable
		
		resultado = valor1 * valor2;
		
		return resultado;
	}
	
	public double dividir (double valor1, double valor2) { //función
		
		double resultado; //declaro la variable
		
		resultado = valor1 / valor2;
		
		return resultado;
	}
	
	public double promediar (double valor1, double valor2, double valor3) { //función
		
		double resultado; //declaro la variable
		
		resultado = (valor1 + valor2 + valor3)/3;
		
		return resultado;
	}
}
